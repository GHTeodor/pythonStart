# Напишіть рекурсивну функцію, щоб порахувати значення факторіалу числа, яке передається
# аргументом (див. рекомендовані ресурси).


def factorial(num):
    if num >= 0:
        return 1 if num == 0 else num * factorial(num - 1)


print(factorial(10))
